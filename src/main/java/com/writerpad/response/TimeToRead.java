package com.writerpad.response;

/**
 * The Class TimeToRead.
 */
public class TimeToRead {
	
	/** The mins. */
	private int mins;
	
	/** The sec. */
	private int sec;
	
	/**
	 * Gets the mins.
	 *
	 * @return the mins
	 */
	public int getMins() {
		return mins;
	}
	
	/**
	 * Sets the mins.
	 *
	 * @param mins the new mins
	 */
	public void setMins(int mins) {
		this.mins = mins;
	}
	
	/**
	 * Gets the sec.
	 *
	 * @return the sec
	 */
	public int getSec() {
		return sec;
	}
	
	/**
	 * Sets the sec.
	 *
	 * @param sec the new sec
	 */
	public void setSec(int sec) {
		this.sec = sec;
	}
	
}
